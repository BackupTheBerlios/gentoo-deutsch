function set_mouseover(id) {
	document.getElementById(id).style.backgroundColor="#eae8f0";
	document.getElementById(id).style.cursor="pointer";
	document.getElementById(id).style.border="1px solid #45347b";
}

function set_mouseout(id) {
	document.getElementById(id).style.backgroundColor="#d3cfe0";
	document.getElementById(id).style.cursor="default";
	document.getElementById(id).style.border="1px solid #d3cfe0";
}
